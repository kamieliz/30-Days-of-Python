# String practice
str = "Hello World"
print(str)
# Single or double quotes doesn't matter still a string
str_single = 'Hola Amigos'
print(str_single)
# Strings can be multiplied
str_multiply = str * 3
print(str_multiply)
# strings can be added to each other - concatenation
str_add = str + str_single
print(str_add)
# to use quotes in the string use an escape character
str_esc = "Hello World, this is \"awesome\"!"
print(str_esc)
# can add line breaks to a string
str_lb = "Hello\nWorld"
print(str_lb)